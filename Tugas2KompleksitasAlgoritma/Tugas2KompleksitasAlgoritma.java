public class Tugas2KompleksitasAlgoritma {
    public static void main(String[] args) {
        int bilangan = 20;
        int hasil = 0;

        for (int i = 0; i < 2; i++) {
            hasil += bilangan;
        }
        
        System.out.println("Hasil dari " + bilangan + " pangkat dua adalah: " + hasil);
    }
}
